    private void btnResetPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetPasswordActionPerformed
        try {
            Random r = new Random();
            int pass = r.nextInt(9999999) + 12345678;
            ForgotPassword fp = new ForgotPassword();
            if (fp.reset(Integer.toString(pass), txtEmail.getText()) > 0) {
                Message ms = new MimeMessage(MailAll.runCheck("tradecoin110@gmail.com", "123456abC!"));
                ms.setFrom(new InternetAddress("tradecoin110@gmail.com"));
                ms.setRecipient(Message.RecipientType.TO, new InternetAddress(txtEmail.getText()));
                ms.setSubject("ResetPassword");
                ms.setText("Password reset: " + pass);
                ms.setSentDate(new Date());
                Transport.send(ms);
                JOptionPane.showMessageDialog(this, "Reset Password Complete");
            }else
            {
                JOptionPane.showMessageDialog(null, "Email Not Found","Error",JOptionPane.ERROR_MESSAGE);
            }
        }catch (AddressException ex) {
            Logger.getLogger(frmForgotPassword.class.getName()).log(Level.SEVERE, null, ex);
        }catch (MessagingException ex) {
            Logger.getLogger(frmForgotPassword.class.getName()).log(Level.SEVERE, null, ex);
        }
        clear();
    }//GEN-LAST:event_btnResetPasswordActionPerformed

